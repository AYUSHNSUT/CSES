struct custom_hash {
    static uint64_t splitmix64(uint64_t x) {
        // http://xorshift.di.unimi.it/splitmix64.c
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(uint64_t x) const {
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};

void solve(){   
    ll n,x;
    cin >> n >> x;
    ll n1 = n / 2;
    ll n2 = (n - n1);
    vl a(n1),b(n2);
    REP(i,n1){
        cin >> a[i];
    }
    REP(i,n2){
        cin >> b[i];
    }
    unordered_map <ll,ll,custom_hash> mpp;
    for(ll mask = 0;mask < (1<<n1);mask++){

        ll t = mask;
        ll maskSum=0;
        ll y = 0;
        while(t){
            if(t&1){
                maskSum += a[y];
                
            }
            y++;
            t >>= 1;
        }
        mpp[maskSum]++;
    }
   ll ans = 0;
   for(ll mask = 0;mask < (1ll<<n2);mask++){

        ll t = mask;
        ll maskSum=0;
        ll y = 0;
        while(t){
            if(t&1){
                maskSum += b[y];
                
            }
            y++;
            t >>= 1;
        }
        if(mpp.count(x-maskSum)){
            ans += mpp[x-maskSum];
        }
    }

    cout << ans;
}



