typedef complex<ll> point;
#define X real()
#define Y imag()
ll orientation(point p1,point p2,point p3){
    ll check = ((p2.Y - p1.Y) * (p3.X - p2.X)) - ((p3.Y - p2.Y)*(p2.X - p1.X));
    if(check == 0) return 0;
    if(check > 0 ) return 1;
    return 2;
}

ll onSegment(point p1,point p2,point p3){
        return (p3.real() >= min(p1.real(),p2.real())&&p3.real() <= max(p1.real(),p2.real()) && (p3.imag() >= min(p1.imag(),p2.imag())&&p3.imag() <= max(p1.imag(),p2.imag())));
}

ll intersecting(point p,point p_, point p1,point p2){

    ll o1 = orientation(p1,p2,p);
    ll o2 = orientation(p1,p2,p_);
    ll o3 = orientation(p,p_,p1);
    ll o4 = orientation(p,p_,p2);

    if(o1 == 0 && onSegment(p1,p2,p)){
        return -1;
    }
    if(o2 == 0 && onSegment(p1,p2,p_)){
        return -1;
    }
    if(o3 == 0 && onSegment(p,p_,p1)){
        return -1;
    }
    if(o4 == 0 && onSegment(p,p_,p2)){
        return -1;
    }

    return (o1!=o2 && o3!=o4);
}
