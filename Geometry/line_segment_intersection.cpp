typedef complex <ll> vect;

ll findOrientation(vect x,vect y,vect z){
    ll num = (y.imag() - x.imag()) * (z.real() - y.real()) -  (z.imag() - y.imag()) * (y.real() - x.real());
    if(num == 0) return 0;
    else if(num > 0) return 1;
    return 2;

}

ll onSegment(vect p1,vect p2,vect p3){
    //check if p3 lies in line p1p2

    return (p3.real() >= min(p1.real(),p2.real())&&p3.real() <= max(p1.real(),p2.real()) && (p3.imag() >= min(p1.imag(),p2.imag())&&p3.imag() <= max(p1.imag(),p2.imag())));
}
void solve(){

    ll a1,a2,a3,a4,a5,a6,a7,a8;
    cin >> a1>>a2>>a3>>a4>>a5>>a6>>a7>>a8;

    vect p1(a1,a2);
    vect p2(a3,a4);
    vect p3(a5,a6);
    vect p4(a7,a8);

    ll o1 = findOrientation(p1,p2,p3);
    ll o2 = findOrientation(p1,p2,p4);
    ll o3 = findOrientation(p3,p4,p1);
    ll o4 = findOrientation(p3,p4,p2);

    if(o1!=o2 && o3!=o4){
        cout << "YES\n";
        return;
    }

    if(!o1 && onSegment(p1,p2,p3)){
        cout << "YES\n";
        return;
        
    }


    if(!o2 && onSegment(p1,p2,p4)){
        cout << "YES\n";
        return;
        
    }


    if(!o3 && onSegment(p3,p4,p1)){
        cout << "YES\n";
        return;
        
    }


    if(!o4 && onSegment(p3,p4,p2)){
        cout << "YES\n";
        return;
        
    }



    cout<< "NO\n";
    return;
    
}



