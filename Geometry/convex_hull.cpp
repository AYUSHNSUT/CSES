ll distSq(point p1,point p2){
    return (p1.X - p2.X)* (p1.X - p2.X) -  (p1.Y - p2.Y) * (p1.Y - p2.Y);
}
ll orientation (point p1,point p2,point p3){
    ll num = (p3.Y - p2.Y) * (p2.X - p1.X) - (p3.X - p2.X) * (p2.Y - p1.Y);
   // DEBUG(num);
    if(num == 0){
        return 0;
    }
    if(num < 0) return 1;
    return 2;
}
