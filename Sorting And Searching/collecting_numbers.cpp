void solve(){
    ll n,m;
    cin >> n >> m;
    vl a(n+2),pos(n+2);
    pos[n+1] = n+1;
   // vl cont(n);
    REP(i,n){
        cin >> a[i+1];
        pos[a[i+1]] = i+1;
    }
    ll ans = 1;
    for(int i = 0;i<n;i++){
        if(pos[i+1] > pos[i+2]){
            ans++;
        //    cont[i] = 1;
        }
    }
   // DEBUG(ans);

    REP(i,m){
        ll x , y;
        cin >> x >>y;
        // DEBUG(x);
        // DEBUG(y);
        ll r = a[x];
        ll s = a[y];
        // DEBUG(r);
        // DEBUG(s);
      if (pos[r-1] <= pos[r] && pos[r-1] > y) ans++;
		if (pos[r-1] > pos[r] && pos[r-1] <= y) ans--;
		if (pos[r] <= pos[r+1] && y > pos[r+1]) ans++;
		if (pos[r] > pos[r+1] && y <= pos[r+1]) ans--;		
		pos[r] = y;
		if (pos[s-1] <= pos[s] && pos[s-1] > x) ans++;
		if (pos[s-1] > pos[s] && pos[s-1] <= x) ans--;
		if (pos[s] <= pos[s+1] && x > pos[s+1]) ans++;
		if (pos[s] > pos[s+1] && x <= pos[s+1]) ans--;	

        swap(a[x],a[y]);

        pos[s] = x;
        cout << ans << endl;
       
    }

}



