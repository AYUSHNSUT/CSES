ll p1 = 1e9 + 7;
ll p2 = 999999929;
ll A = 233;
class hashRoll{
    public:
    ll size;
    ll primeMultipler;
    vl hashedVector1;
    vl hashedVector2;
    vl primeFactr1;
    vl primeFactr2;
    ll m1;
    ll m2;

    void init1(string &s, ll passedP = p1, ll passedM = A ){
        size = s.size();
        hashedVector1.assign(size+1,0);
        primeFactr1.assign(size+1,1);
        primeMultipler = passedM;
        m1 = passedP;
        for(int i=1;i<=size;i++)
        {
            primeFactr1[i] *=  (primeMultipler * primeFactr1[i-1]);
            primeFactr1[i] %=M;

            hashedVector1[i] = hashedVector1[i-1]*primeMultipler + s[i-1];
            hashedVector1[i]%=M;
        }
    }

    ll return1(ll l , ll r){
        ll pref = (hashedVector1[l]*primeFactr1[r-l+1])%m1;
        ll diff = hashedVector1[r+1] - pref;

        if(diff < 0){
            return diff + m1;
        }
        return diff;

    }
};

void solve(){
    string str;
    cin>>str;
    int n=str.size();
    str+=str;
    hashRoll h;
    h.init1(str);

    int ans=0;
    int ct=0;
    for(int i=1;i<n;i++)
    {
        int low=1;
        int high=n-1;
        int c=0;
        while(low <= high)
        {
            int len=(high+low)/2;
            ct++;
            if(h.return1(ans,ans+len-1)==h.return1(i,i+len-1))
            {
                low=len+1;
                c=len;
            }
            else
            {
                high=len-1;
            }
        }
        if(str[i+c]<str[ans+c])
            ans=i;
    }
    cout<<str.substr(ans,n);
}



