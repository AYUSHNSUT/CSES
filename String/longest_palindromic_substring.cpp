string LongPalSubs(string &s){
    int l = 0, r = -1;
    int mirror = 0;
    // DEBUG(s);

    vi LPS(s.size());
    for(int i = 1;i<s.size()-1;i++){
        int prevFound = 2*mirror - i;
        if(i < r){
            LPS[i] = min(LPS[prevFound], r-i);
        }

        while(s[i + LPS[i] + 1] == s[i - LPS[i] - 1]){
            LPS[i]++;
        }

        if(r < i + LPS[i]){
            r = i + LPS[i];
            mirror = i;
        }
    }
    int mx = 1;
    int id = 2;
    int ii = -1;
    for(auto k : LPS){
        ii++;
        // DEBUG(ii);
        // DEBUG(s[ii]);
        // DEBUG(k);
        if(mx < k){
            mx = k;
            id = ii;
        }
    }

    // DEBUG(id);
    // DEBUG(s[id]);

    vector <char> c;
    if(s[id] != '#')
       {
              mx--;
            c.pb(s[id]);
        }

 
    mx/=2;

//    DEBUG(mx);
    for(int i = id + 1;mx;i++){
        if(s[i]!= '#' && s[i]!='@' && s[i]!= '&')
        {c.pb(s[i]);
        mx--;}
    }

    if(s[id] == '#'){
        string first(all(c));
        string second = first;
        reverse(all(first));

        return first + second;
    }
    else{
        auto it = c.begin();
        it++;
        string second(it,c.end());
        reverse(all(second));

        return second + string(all(c));
    }
    return "";
}
