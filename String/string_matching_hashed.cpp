ll substringHash(vl &hashVector, ll a, ll b, ll m ,unordered_map<ll,ll> &primePower){
    if(a == 0){
        return hashVector[b];
    }
    return ((hashVector[b] - (hashVector[a-1]*primePower[b-a+1])%m)%m+m)%m;
}

